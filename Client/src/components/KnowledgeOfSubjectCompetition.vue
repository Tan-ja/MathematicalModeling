            一、全国大学生数学建模竞赛

            全国大学生数学建模竞赛的口号是“ 一次参赛，终生受益！”可想而知他的含金量。

            该竞赛创办于1992年，每年一届，是首批列入“高校学科竞赛排行榜”的19项竞赛之一。

            2019年，来自全国及美国和马来西亚的1490所院校/校区、42992队(本科39293队、专科3699队)、近13万人报名参赛。

            2020年，来自全国及美国、英国、马来西亚的1470所院校/校区、45680队(本科41826队、专科3854队)、13万多人报名参赛。

            参赛者以 3 名大学生组成一队（鼓励不写指导教师），通过学校教务部门向所在赛区组委会报名，再由赛区组委会向全国组委会报名。若所在地区尚未成立赛区，由学校直接向全国组委会报名。

            需要注意的是，同一参赛队的学生必须来自同一所学校。2020年大赛举办时间：9 月 10 日（周四）18 时至 9 月 13 日（周日）20 时

            官方地址：http://www.mcm.edu.cn/

