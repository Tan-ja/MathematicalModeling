            十一、全国大学生广告艺术大赛

            全国大学生广告艺术大赛(大广赛)--中国最大的高校广告艺术传播平台，是由教育部高等教育司指导、教育部高等学校新闻传播学类专业教学指导委员会、中国高等教育学会广告教育专业委员会共同主办，中国传媒大学、全国大学生广告艺术大赛组委会承办的全国高校文科大赛。大广赛是迄今为止全国规模最大、覆盖高等院校较广、参与师生人数多、国家级大学生赛事。

            2005年第一届大广赛开赛，成功举办了九届十次赛事，全国一千三百多所高校参与其中，数十万学生提交作品，形成了稳定的、成熟的、具有相当规模的大学生教学实践平台。

            官方地址：http://www.sun-ada.net/

            竞赛解析：全国大学生广告艺术大赛



            十二、全国大学生智能汽车竞赛

            全国大学生智能汽车竞赛是以智能汽车为研究对象的创意性科技竞赛，是面向全国大学生的一种具有探索性工程实践活动，是教育部倡导的大学生科技竞赛之一。

            本竞赛以“立足培养，重在参与，鼓励探索，追求卓越”为指导思想，旨在促进高等学校素质教育，培养大学生的综合知识运用能力、基本工程实践能力和创新意识，激发大学生从事科学研究与探索的兴趣和潜能,倡导理论联系实际、求真务实的学风和团队协作的人文精神，为优秀人才的脱颖而出创造条件。

            本竞赛以竞速赛为基本竞赛形式，辅助以创意赛和技术方案赛等多种形式。竞速赛以统一规范的标准硬软件为技术平台，制作一部能够自主识别道路的模型汽车，按照规定路线行进，并符合预先公布的其他规则，以完成时间最短者为优胜。创意赛是在统一限定的基础平台上，充分发挥参赛队伍想象力，以创意任务为目标，完成研制作品；竞赛评判由专家组、现场观众等综合评定。技术方案赛是以学术为基准，通过现场方案交流、专家质疑评判以及现场参赛队员和专家投票等互动形式，针对参赛队伍的优秀技术方案进行评选,其目标是提高参赛队员创新能力，鼓励队员之间相互学习交流。

            本竞赛过程包括理论设计、实际制作、整车调试、现场比赛等环节，要求学生组成团队，协同工作，初步体会一个工程性的研究开发项目从设计到实现的全过程。竞赛融科学性、趣味性和观赏性为一体，是以迅猛发展、前景广阔的汽车电子为背景，涵盖自动控制、模式识别、传感技术、电子、电气、计算机、机械与汽车等多学科专业的创意性比赛。本竞赛规则透明，评价标准客观，坚持公开、公平、公正的原则，保证竞赛向健康、普及、持续的方向发展。

            官方地址：https://smartcar.cdstm.cn/index/2.html



