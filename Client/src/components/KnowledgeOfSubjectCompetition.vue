            二、全国大学生结构设计竞赛

            全国大学生结构设计竞赛由教育部、财政部首次联合批准发文的全国性 9 大学科竞赛资助项目之一，目的是为构建高校工程教育实践平台，进一步培养大学生创新意识、团队协同和工程实践能力，切实提高创新人才培养质量。该竞赛由中国高等教育学会工程教育专业委员会、高等学校土木工程学科专业指导委员会、中国土木工程学会教育工作委员会和教育部科学技术委员会环境与土木水利学部共同主办，各高校轮流承办和社会企业资助协办。

            凡参赛高校只允许申报 1 支队伍参赛，每队由 3 名全日制在校本科或专科生、1~2 名指导教师（3 人以上署指导组）和 1 名领队组成；承办全国竞赛高校可申报 2 支队伍参赛。

            2020竞赛时间：分赛时间：2020年4月至7月上

            官方地址：http://www.structurecontest.com/

