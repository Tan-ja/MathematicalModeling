            根据参赛学生层次设定本科组和专科组两套竞赛题目，包括“理论知识”和“技能操作”两部分，以地学基本知识和技能应用为基础，主要涉及古生物、地层、矿物、岩石、矿床、构造、地球物理、地球化学及其它相关学科基本技能，既考虑一般学生能达到基本要求，又给优秀学生充分发挥与创新的余地。

            竞赛题目具实际意义和应用背景，结合地学教学的基本内容和新技术的应用趋势，着重考核参赛学生综合运用基础知识进行理论设计和实践工作的基本能力和综合技能，鼓励参赛学生崇尚地球科学、探索地球奥秘、投身地质事业。



